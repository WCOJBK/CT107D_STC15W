//  返回距离
uchar distance_measure()
{
	uchar dist = 0;
	uint dist_uint;
	uint t = 0;
	

	sonic_gen();

	TR0 = 1;
	while ((RX == 1) && (TF0 == 0));  // 等待回波或是计数器溢出
	TR0 = 0;

	if (TF0 == 1)
	{
		dist = 99;
		TH0 = TL0 = 0;
		TF0 = 0;
	}
	else
	{
		t = TH0;
		t <<= 8;
		t |= TL0;
		dist_uint = (uint)(t * 0.017);
		if (dist_uint > 99)
		{
			dist = 99;
		}
		else
		{
			dist = dist_uint;
		}
		TH0 = TL0 = 0;
		TF0 = 0;
	}
	return dist;
}